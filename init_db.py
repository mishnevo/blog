"""
Скрипт инициализации базы данных.
Запустить один раз перед первым стартом приложения:
    python init_db.py
"""

import sqlite3

connection = sqlite3.connect('database.db')

with open('schema.sql') as f:
    connection.executescript(f.read())

cur = connection.cursor()

sample_posts = [
    ('Добро пожаловать в блог!',
     'Это первый пост нашего блога. Здесь вы найдёте интересные статьи на разные темы. '
     'Используйте REST API для программного доступа к постам.'),
    ('REST API: что это такое?',
     'REST (Representational State Transfer) — это архитектурный стиль для создания веб-сервисов. '
     'В нашем блоге реализованы все основные HTTP-методы: GET, POST, PUT, DELETE.'),
    ('Пагинация в Flask',
     'Пагинация позволяет разбивать большой список постов на страницы. '
     'В нашем приложении на каждой странице отображается 5 постов.'),
    ('Экспорт данных в CSV',
     'CSV (Comma-Separated Values) — удобный формат для обмена табличными данными. '
     'Нажмите кнопку «Экспорт в CSV» на главной странице, чтобы скачать все посты.'),
    ('Аутентификация через API-ключ',
     'Для защиты API используется ключ, который передаётся в заголовке X-API-Key. '
     'Это простой и эффективный способ ограничить доступ к API.'),
    ('Flask — микрофреймворк для Python',
     'Flask — это лёгкий веб-фреймворк для Python. Он предоставляет только базовые инструменты, '
     'оставляя разработчику свободу выбора дополнительных компонентов.'),
    ('SQLite: встроенная база данных',
     'SQLite — это встраиваемая реляционная СУБД, не требующая отдельного сервера. '
     'Идеально подходит для небольших проектов и прототипов.'),
]

cur.executemany(
    "INSERT INTO posts (title, content) VALUES (?, ?)",
    sample_posts
)

connection.commit()
connection.close()

print("✅ База данных успешно создана и заполнена тестовыми данными!")
print(f"   Добавлено постов: {len(sample_posts)}")
